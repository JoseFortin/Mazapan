package com.example.mazapan.Entidades;

public class Cursos {

    private int Id_Cursos;
    private String Nombre_Curso;
    private String Descripcion_curso;


    public int getId_Cursos() {
        return Id_Cursos;
    }

    public void setId_Cursos(int id_Cursos) {
        Id_Cursos = id_Cursos;
    }

    public String getNombre_Curso() {
        return Nombre_Curso;
    }

    public void setNombre_Curso(String nombre_Curso) {
        Nombre_Curso = nombre_Curso;
    }

    public String getDescripcion_curso() {
        return Descripcion_curso;
    }

    public void setDescripcion_curso(String descripcion_curso) {
        Descripcion_curso = descripcion_curso;
    }
}
